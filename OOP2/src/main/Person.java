package main;

public class Person {
	
	String firstName;
	String lastName;
	String address;
	char sex;
	int age;
	
	
	Person(String firstName, String lastName, String address, char sex, int age){
		
		this.firstName = firstName;
		this.lastName = lastName;
		this.address = address;
		
		this.sex = sex;
		this.age = age;
	
		
	}

}
